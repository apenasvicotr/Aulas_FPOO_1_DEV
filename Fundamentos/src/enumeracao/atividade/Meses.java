
package enumeracao.atividade;
public class Meses {
    
    private String meses;

    public Meses(String meses) {
        this.meses = meses;
    }

    public void setMeses(String meses) {
        this.meses = meses;
    }

    @Override
    public String toString() {
        return "Meses{" + "meses=" + meses + '}';
    }
    
    
    
}
