
package enumeracao.atividade;

public class Itens {
    
    private String itens;

    public Itens(String itens) {
        this.itens = itens;
    }

    public void setItens(String itens) {
        this.itens = itens;
    }

    @Override
    public String toString() {
        return "Itens{" + "itens=" + itens + '}';
    }
    
    
    
}
