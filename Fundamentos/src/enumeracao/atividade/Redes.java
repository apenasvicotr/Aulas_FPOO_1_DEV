
package enumeracao.atividade;

public class Redes {
    
    private String redes;

    public Redes(String redes) {
        this.redes = redes;
    }

    public void setRedes(String redes) {
        this.redes = redes;
    }

    @Override
    public String toString() {
        return "Redes{" + "redes=" + redes + '}';
    }
    
    
    
}
